            <h4>Internal Quality Assurance Cell (IQAC)</h4>
            <p>The basic purpose of establishment of IQAC is to improve the academic and administrative performance of the institution. Quality enhancement is a continuous process to the institution towards academic excellence. The publication of IQAC NEWS helps to broadcast the information about the institutional activities, interaction carried out by an institution towards the best outcome of educational strategy.</p>
            <h5>Objectives of IQAC</h5>
            <ul class="ul_style">
                  <li>To develop a system for conscious, consistent and catalytic action for the improvement of academic and administrative performance of the institution.</li>
                  <li>To promote measures for institutional functioning towards quality enhancement through internalization of quality culture and institutionalization of best practices.</li>
            </ul>
            <h5>Strategies</h5>
            <p>IQAC shall evolve mechanisms and procedures for,</p>
            <ul class="ul_style">
                  <li>Ensure timely, efficient and progressive performance of academic, administrative tasks</li>
                  <li>Conduct quality based academic standards and research programmes</li>
                  <li>Optimize and integrate the modern methods of teaching and learning process.</li>
                  <li>Asses and evaluate the progress of institutional activities.</li>
                  <li>Sharing of research innovations and inventions with best outcome.</li>
            </ul>
            <h5>Functions</h5>
            <ul class="ul_style">
                  <li>Initiating the various academic and administrative activities of the institution.</li>
                  <li>Facilitating the construction of a learning environment towards quality education for participatory teaching and learning process.</li>
                  <li>Organizing workshops and seminars on Pharmaceutical research and development in association with national, international universities and pharmaceutical industry.</li>
                  <li>Documentation of the various programmes and activities organized in institute for quality improvement.</li>
                  <li>Coordinating the quality-related activities, including adoption of best practices.</li>
                  <li>Periodical conduct of Academic Audit.</li>
            </ul>
